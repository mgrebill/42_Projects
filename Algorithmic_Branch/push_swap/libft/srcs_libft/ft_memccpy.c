#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t			i;
	unsigned char	u;
	unsigned char	*d;
	unsigned char	*s;

	i = 0;
	u = (unsigned char)c;
	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	while (i < n)
	{
		d[i] = s[i];
		if (s[i] == u)
			return (&d[i + 1]);
		i++;
	}
	return (NULL);
}
